'''
abs(num)

- Returns the absolute value of the given number. If the number is a complex 
number, abs() returns its magnitude.
- a number whose absolute value is to be returned. The number can be integer, 
floating number or complex number.
'''

integer = -10
floating = -3.14
complex = 3 + 4j

print(abs(integer))
print(abs(floating))
print(abs(complex)) # Return Magnitude